package com.xkyss.json.bean;


public class Bar {

    private String nullProperty;

    private String emptyProperty;

    private String ignoreProperty;

    private String a;

    private String b;

    public String getNullProperty() {
        return nullProperty;
    }

    public void setNullProperty(String nullProperty) {
        this.nullProperty = nullProperty;
    }

    public String getEmptyProperty() {
        return emptyProperty;
    }

    public void setEmptyProperty(String emptyProperty) {
        this.emptyProperty = emptyProperty;
    }

    public String getIgnoreProperty() {
        return ignoreProperty;
    }

    public void setIgnoreProperty(String ignoreProperty) {
        this.ignoreProperty = ignoreProperty;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}